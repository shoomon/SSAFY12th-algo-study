package 아이고아이고;

import java.util.Scanner;

public class _14500_테트로미노 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		int N = sc.nextInt();
		int M = sc.nextInt();
		int[][] arr = new int[N][M];
		for (int i = 0; i < N; i++) {
			for (int j = 0; j < M; j++) {
				arr[N][M] = sc.nextInt();
			}
		}

		// 빡구현은 오바적인거 같아서..
		// 다른 방법 생각하다가 못풀었어욥..
		
	} // end of main
} // end of class
